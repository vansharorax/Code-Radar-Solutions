#include <stdio.h>
int main() {
    int year;
    scanf("%d",&year);

    if ( year = 2000) {
        printf("Leap Year");
    } else if ( year = 1900) {
        printf("Leap Year");
    } else if (year = 2023) {
        printf("Leap Year");
    } else {
        printf("Not a Leap Year");
    }
    return 0;
}